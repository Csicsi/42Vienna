#include "push_swap.h"

static void	rotate(t_stack *stack)
{
	if (stack == NULL || stack->size == 0)
		return ;
	stack->head = stack->head->next;
}

void	ra(t_stack *a)
{
	rotate(a);
	ft_printf("ra\n");
}

void	rb(t_stack *b)
{
	rotate(b);
	ft_printf("rb\n");
}

void	rr(t_stack *a, t_stack *b)
{
	rotate(a);
	rotate(b);
	ft_printf("rr\n");
}
